import type { LegalConfig } from "../../types.js";

/**
 * Build the runtime @legal module (v1).
 *
 * RESPONSIBILITY:
 * - Convert validated LegalConfig into a runtime JS module
 *
 * HARD GUARANTEES:
 * - Pure function (no fs, no env)
 * - Never throws
 * - Always emits valid JavaScript
 * - Deterministic output
 *
 * v1 INVARIANTS:
 * - This function is ONLY called with a validated config
 * - `paths.layout` is REQUIRED and always a string
 * - Optionality has already been resolved by reader.ts
 *
 * SINGLE SOURCE OF TRUTH:
 * - Everything exposed here is what policy templates may consume
 */
export function buildLegalModule(config: LegalConfig): string {
  const organisation = config.organisation;
  const contacts = config.contacts;
  const paths = config.paths;

  return `
// ⚠️ AUTO-GENERATED — DO NOT EDIT
// Generated by astro-legal (v1)

export const legal = {
  /**
   * Organisation metadata
   */
  organisation: {
    name: ${JSON.stringify(organisation.name ?? "This service")},
    jurisdiction: ${JSON.stringify(
      organisation.jurisdiction ?? "the applicable jurisdiction"
    )},
    address: ${JSON.stringify(
      organisation.address ?? "Not publicly listed"
    )},
    companyNumber: ${JSON.stringify(
      organisation.companyNumber ?? "Not applicable"
    )},
  },

  /**
   * Contact addresses
   *
   * These values are injected into policy templates.
   * Individual policies may require specific fields.
   */
  contacts: {
    privacyEmail: ${JSON.stringify(
      contacts.privacyEmail ?? "privacy@example.com"
    )},
    legalEmail: ${JSON.stringify(
      contacts.legalEmail ?? "legal@example.com"
    )},
    abuseEmail: ${JSON.stringify(
      contacts.abuseEmail ?? "abuse@example.com"
    )},
    lawEnforcementEmail: ${JSON.stringify(
      contacts.lawEnforcementEmail ?? "law@example.com"
    )},
    securityEmail: ${JSON.stringify(
      contacts.securityEmail ?? "security@example.com"
    )},
  },

  /**
   * Path & layout configuration (v1 strict)
   *
   * GUARANTEE:
   * - layout is ALWAYS present and valid
   * - Missing layout would have disabled the plugin earlier
   */
  paths: {
    base: ${JSON.stringify(paths.base)},
    layout: ${JSON.stringify(paths.layout)},
  },

  /**
   * Effective date (config, ISO-8601)
   */
  effectiveDate: ${JSON.stringify(config.effectiveDate)},
};
`;
}
